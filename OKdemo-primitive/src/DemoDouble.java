public class DemoDouble {
  public static void main (String[] args) {

    double d1= 0.2; // if no notation, default it is double value
    double d10 = 0.8d; // d is the notation of double
    double d2= 0.3;
    double d3= d1 + d2;
    System.out.println(d3); // 0.5

    double d4 = 1999.31123912302110109;
    System.out.println(d4); 

    float f1= 0.2f; // f is notation of float // 0.2 is double d1 on line 4

    float f2 = 19999.3112391230211010f;
    System.out.println(f2);


  }
  
}
